// Examples of var in Kotlin
fun main() {
    var count = 1
    println("Count is $count")
    
    count = 2
    println("Count is now $count")

    var greeting: String
    greeting = "Hello"
    println("$greeting, world!")

    var pi: Double = 3.14
    pi = 3.14159
    println("The value of pi is $pi")
}
